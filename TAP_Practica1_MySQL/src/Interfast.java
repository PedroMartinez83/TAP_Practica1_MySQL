    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int confirmado;
        Component componentePadre = null;
        confirmado = JOptionPane.showConfirmDialog(
                componentePadre,
                "Â¿ESTAS SEGURO QUE QUIERES BORRAR ESTE RENGLON?");

if (JOptionPane.OK_OPTION == confirmado)
   System.out.println("Borrado con exito");

else
   System.out.println("no se borro");
        int renglonSeleccionado = jTable1.getSelectedRow();
        if(renglonSeleccionado == -1){
            JOptionPane.showMessageDialog(this, "Error, debes seleccionar un renglon a borrar");
            return;
        }
        String idBorrar = tabla.getValueAt(renglonSeleccionado, 0).toString();
        boolean resultadoEliminar = mySQL.eliminar(idBorrar);
        if(resultadoEliminar){
            actualizarJTable();
            JOptionPane.showMessageDialog(this, "Se borro el renglon");
        }else{
            JOptionPane.showMessageDialog(this, "Error, no se pudo borrar");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

